'''
Son símbolos o palabras especiales que permiten
realizar operaciones sobre variables y valores
'''

# Operadores aritméticos 
# Sirven para cálculos matemáticos:
x + y   # Suma
x - y   # Resta
x * y   # Multiplicación
x / y   # División
x % y   # Módulo (resto)
x ** y  # Potencia (x elevado a y)
x // y  # División entera (sin decimales)

# Operadores de Asignación
# Sirven para asignar valores a variables:
x = 5   # Asignación simpple
x += 3  # x = x + 3
x -= 2  # x = x - 2
x *= 4  # x = x * 4
x /= 2  # x = x / 2

# Operadores de Comparación
# Sirven para comparar valores (devuelven True o False):
x == y  # Igual a
x != y  # Distinto que
x > y   # Mayor que
x < y   # Menor que
x >= y  # Mayor o igual que
x <= y  # Menor o igual que

# Operadores Lógicos
# Combinan condiciones lógicas:
x and y # Y lógico
x or y  # O lógico
not x   # Negación lógica

# Operadores de Membresía
# Sirven para verificar si un valor está dentro de una secuencia:
x in lista      # verifica si x está en lista
x not in lista  # verifica si x no está en lista

# Operadores de Identidad
# Comprueban si dos variabls apuntan al mismo objeto:
x is y      # verdadero si x y y son el mismo objeto
x is not y  # verdadero si no lo son
