# Tipo de datos:
'''
Determinan qué valor almacena una variable
y qué operaciones puede hacer con ella
'''

# int = enteros (10)
# float = decimales (3.14)
# str = texto ("Hola")
# bool = valores lógicos (True o False)
# list, tuple, dict, set = estructuras de datos más complejas

# Tipos Numéricos:
# int: números enteros
edad = 30

# float: números decimales
pi = 3.14159

# complex: números complejos (menos usados)
z = 2 + 3j

# Tipos de Texto:
# str: Cadeas de texto:
nombre = "Ana"

# Tipos Booleanos
# bool: solo puede ser True o False:
activo = True
inactivo = False

# Tipos de Secuencia
# list: lista mutable (puede cambiar)
frutas = ["manzanas", "plátano"]

# tuple: Tupla inmutable (no puede cambiar)
coordenadas (10.0, 20.0)

# range: secuencia de números
numeros = range(5) # 0, 1, 2, 3, 4

# Tipos Conjuntos
# set: conjunto sin duplicados
colores = {"rojo", "verde", "azul", "amarillo"}
# frozenset: conjunto inmutable

# Tipo de Mapeo
# dict: Diccionario (clave: valor)
persona = {"nombre": "Ana", "edad": 30}

# Tipo NoneType
# None: representa ausencia de valor
resultado = None

# Conversión de Tipos (Casting)
# Puedes cambiar un tipo a otro si es compatible:
x = int("5")    # convierte string a int (5)
x = float(10)   # convierte int a float (10.0)
z = str(3.14)   # convierte float a string ("3.14")

# Ejemplo que mezcla varios tipos:

nombre = "Luis" # string
edad = 25       # int
altura = 1.75   # float
activo = True   # bool
hobbies = ["leer", "fútbol"]    # list
